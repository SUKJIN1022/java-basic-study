package variable;

public class var5 {

    public static void main(String[] args) {
        int a; // 변수선언
         a = 1; // 초기화를 각각 따로
        System.out.println(a);

        int b = 2; // 변수를 선언하고 초기화를 한번에
        System.out.println(b);
        int c = 3, d =4; // 여러 변수를 선언하고 초기화 한번에
        System.out.println(c);
        System.out.println(d);
    }
}
