package operator;

public class Com1 {

    public static void main(String[] args) {
        int a = 2;
        int b = 3;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);

        //boolean 변수에 담을수있다.
        boolean result = a == b;
        System.out.println(result);

    }
}
